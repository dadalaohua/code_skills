    if(x[3] == 3[x])
        printf("x[3] == 3[x]\n");
    
    for(int i = 0; i < 5; i++)
    {
        if(x[i] == i[x])
            printf("x[%d] == %d[x]\n", i, i);
    }
    
    char y = "0123456789abcdef"[5];
    printf("%c\n", y);
    
    char z = 5["0123456789abcdef"];
    printf("%c\n", z);
    
