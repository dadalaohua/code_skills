int main(int argc, char* argv[])
{ 
    int a;
    
    printf("请输入一个整数值： ");
    scanf("%d", &a);
	
	//为真时继续运行
	//为假时输出诊断消息
    assert(a >= 10);    //输入数值小于10则出错，程序终止
    printf("输入的整数是： %d\n", a);
   
    return 0;
}