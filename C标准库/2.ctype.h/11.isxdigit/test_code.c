int main(int argc, char* argv[])
{ 
    char str[]="123c@#hello[world?";
    int i;
    
    for(i = 0; str[i]!='\0'; i++)
    {
        if(isxdigit(str[i]))
        {
            printf("%c 是一个十六进制数\n",str[i]);
        }
    }

    return 0;
}