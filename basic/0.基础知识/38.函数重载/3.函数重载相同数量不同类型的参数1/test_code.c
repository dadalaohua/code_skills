#include <stdio.h>
#include <stdarg.h>

/************************************************************************/
/*                                                                      */
/************************************************************************/

/************************************************************************/
/*                                                                      */
/************************************************************************/

void test1(void * a, void * b)
{
    printf("test1 %d %d\n", *(int *)a, *(int *)b);
}

void test2(void * a, void * b)
{
    printf("test2 %f %f\n", *(float *)a, *(float *)b);
}

typedef void (* testfunc)(void *, void *);
/************************************************************************/
/*                                                                      */
/************************************************************************/

int main(int argc, char* argv[])
{
    int a,b;
    float x,y;
    testfunc test;
    
    test = test1;
    a = 10;
    b = 20;
    test(&a, &b);
    
    test = test2;
    x = 112.34;
    y = 66.88;
    test(&x, &y);
    
    return 0;
}