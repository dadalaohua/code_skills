int main(int argc, char* argv[])
{
    double val = 1.234567891234; //12位小数
    printf("%f\n", val);    //只会输出6位小数 1.234568
    printf("%lf\n", val);   //只会输出6位小数 1.234568
    
    printf("%.3f\n", val);  //输出3位小数    1.235
    printf("%.8f\n", val);  //输出8位小数    1.23456789
    printf("%.10f\n", val); //输出10位小数   1.2345678912
    printf("%.12f\n", val); //输出12位小数   1.234567891234
    
    return 0;
}