#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <math.h>

/************************************************************************/
/*                                                                      */
/************************************************************************/
#define PI 3.14159265

//如何手算任意角度正弦值
//https://zhuanlan.zhihu.com/p/376215027
/************************************************************************/
/*                                                                      */
/************************************************************************/

int main(int argc, char* argv[])
{
    //计算范围sin0° - sin15°
    for(int i = 0; i <= 15; i++) {
        printf("%2d°: %f\n", i, i * PI / 180);
    }
    printf("\n");
    
    //计算范围sin15° - sin30°
    //sin15° ≈ 0.258819
    //sin30° = 0.500000
    //(sin30° - sin15°) / 15 = (0.500000 - 0.258819) / 15 = 0.241181 / 15 = 0.016078733 ≈ 0.016079
    for(int i = 0; i <= 15; i++) {
        printf("%2d°: %f\n", i + 15, 0.258819 + i * 0.016079);
    }
    printf("\n");
    
    //计算范围sin30° - sin45°
    //sin45° ≈ 0.707107
    //sin30° = 0.500000
    //(sin45° - sin30°) / 15 = (0.707107 - 0.500000) / 15 = 0.207107 / 15 = 0.0138071333 ≈ 0.013807
    for(int i = 0; i <= 15; i++) {
        printf("%2d°: %f\n", i + 30, 0.5 + i * 0.013807);
    }
    printf("\n");
    
    //计算范围sin45° - sin60°
    //sin45° ≈ 0.707107
    //sin60° ≈ 0.866025
    //(sin60° - sin45°) / 15 = (0.866025 - 0.707107) / 15 = 0.158918 / 15 = 0.010594533 ≈ 0.010595
    for(int i = 0; i <= 15; i++) {
        printf("%2d°: %f\n", i + 45, 0.707107 + i * 0.010595);
    }
    printf("\n");
    
    //计算范围sin60° - sin75°
    //sin60° ≈ 0.866025
    //sin75° ≈ 0.965926
    //(sin75° - sin60°) / 15 = (0.965926 - 0.866025) / 15 = 0.099901 / 15 ≈ 0.006660
    for(int i = 0; i <= 15; i++) {
        printf("%2d°: %f\n", i + 60, 0.866025 + i * 0.006660);
    }
    printf("\n");
    
    //计算范围sin75° - sin90°
    //sin75° ≈ 0.965926
    //sin90° = 1
    //(sin90° - sin75°) / 15 = (1 - 0.965926) / 15 = 0.034074 / 15 = 0.0022716 ≈ 0.002272
    for(int i = 0; i <= 15; i++) {
        printf("%2d°: %f\n", i + 75, 0.965926 + i * 0.002272);
    }
    printf("\n");
    
//方式二
    //计算范围sin45° - sin60°
    //A + B = 90°
    //sinA ^ 2 + sinB ^ 2 = 1
    //sinA ^ 2 = 1 - sinB ^ 2
    //sinA ^ 2 = 1 - sin(90 - A) ^ 2
    //sinA = sqrt(1 - sin(90 - A) ^ 2)
    for(int i = 0; i <= 15; i++) {
        printf("%2d°: %f\n", i + 45, sqrt(1 - (0.5 + (15 - i) * 0.013807) * (0.5 + (15 - i) * 0.013807)));
    }
    printf("\n");
    
    //计算范围sin60° - sin75°
    for(int i = 0; i <= 15; i++) {
        printf("%2d°: %f\n", i + 60, sqrt(1 - (0.258819 + (15 - i) * 0.016079) * (0.258819 + (15 - i) * 0.016079)));
    }
    printf("\n");
    
    //计算范围sin75° - sin90°
    for(int i = 0; i <= 15; i++) {
        printf("%2d°: %f\n", i + 75, sqrt(1 - ((15 - i) * PI / 180) * ((15 - i) * PI / 180)));
    }
    printf("\n");
    
    return 0;
}