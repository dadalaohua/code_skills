#include <stdio.h>
#include <math.h>

/************************************************************************/
/*                                                                      */
/************************************************************************/
#define PI 3.14159265

/************************************************************************/
/*                                                                      */
/************************************************************************/

/************************************************************************/
/*                                                                      */
/************************************************************************/

int main(int argc, char* argv[])
{
    double i;
    
    for(i = 0; i < 1; i += 0.05)
    {
        printf("The arc sine of %f is %f degrees\n", i, asin(i) * 180 / PI);
    }
    
    printf("The arc sine of %f is %f degrees\n", i, asin(1) * 180 / PI);
    
    printf("\n");
    
    return 0;
}