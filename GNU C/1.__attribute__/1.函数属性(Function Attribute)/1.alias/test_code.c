#include <stdio.h>

/*
__attribute__((alias))  函数属性使你能够为一个函数指定多个别名。
*/

/************************************************************************/
/*                                                                      */
/************************************************************************/
void foo(void)
{
    printf("%s\n", __FUNCTION__);
}

void bar(void) __attribute__((alias("foo")));

/************************************************************************/
/*                                                                      */
/************************************************************************/

int main(int argc, char* argv[])
{
    bar(); // calls foo
    
    return 0;
}