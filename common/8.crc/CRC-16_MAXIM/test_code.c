//查表法计算crc
unsigned short do_crc_table(unsigned char *ptr, int len)
{
    unsigned char data;
    unsigned short crc = CRC_INIT;
    
    while(len-- > 0) 
    {
        crc = (crc >> 8) ^ crc_table[(crc ^ *ptr++) & 0xff];
    }
    
    crc ^= 0xffff;
    
    return (crc);
}

//直接计算法计算crc
unsigned short do_crc(unsigned char *ptr, int len)
{
    unsigned int i;
    unsigned short crc = CRC_INIT;
    
    while(len--)
    {
        crc ^= *ptr++;
        for (i = 0; i < 8; ++i)
        {
            if (crc & 1)
                crc = (crc >> 1) ^ 0xA001;        // 0xA001 = reverse 0x8005
            else
                crc = (crc >> 1);
        }
    }
    
    return ~crc;
}
